    public class runxoaxe implements Runnable {

        @Override
        public void run() {
            try {
            socket = new Socket(ip, 2000);
            din = new DataInputStream(socket.getInputStream());
            dout = new DataOutputStream(socket.getOutputStream());
            dout.writeUTF("yeucauxoaxe&"+txid);
        } catch (IOException ex) {
            Logger.getLogger(XoaXe.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String bien=null;
        while(true) {
            try {
                bien = din.readUTF();
                if(bien.equalsIgnoreCase("hetxe")) break;
                vtt.add(bien);
            } catch (IOException ex) {
                Logger.getLogger(KhoiTaoTaiXe.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        jComboBox1.setModel(new DefaultComboBoxModel(vtt));
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String bien = jComboBox1.getSelectedItem().toString();
        if(jComboBox1.getItemCount()==1) {
            JOptionPane.showMessageDialog(null, "Ban co duy nhat 1 xe, khong the xoa\nNeu that su muon xoa, lam on them xe khac");
            try {
            dout.writeUTF("quit");
            } catch (IOException ex) {
                Logger.getLogger(ThemXe.class.getName()).log(Level.SEVERE, null, ex);
            }
            dispose();
        }
        if(bien.equalsIgnoreCase(biensoxe)) {
            JOptionPane.showMessageDialog(null, "Ban dang dang nhap cung voi chiec xe nay, khong the xoa\nNeu that su muon xoa, lam on doi xe khac");
            try {
            dout.writeUTF("quit");
            } catch (IOException ex) {
                Logger.getLogger(ThemXe.class.getName()).log(Level.SEVERE, null, ex);
            }
            dispose();
        }
        try {
            dout.writeUTF("xoaxe&"+bien);
        } catch (IOException ex) {
            Logger.getLogger(ThemXe.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            dout.writeUTF("quit");
        } catch (IOException ex) {
            Logger.getLogger(ThemXe.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

